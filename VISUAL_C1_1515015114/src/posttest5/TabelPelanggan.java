    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            int baris = Tabel.getSelectedRow();
            Tabel.setValueAt(txt_Nama.getText(), baris, 0);
            Tabel.setValueAt(txt_Tlp.getText(), baris, 1);
            Tabel.setValueAt(cbKota.getSelectedItem(), baris, 3);
        } catch (Exception e){
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        try {
            int pilih = JOptionPane.showConfirmDialog(this,"ingin hapus","Confirmasi hapus ",JOptionPane.YES_OPTION);
            if(pilih==JOptionPane.YES_OPTION){
                int baris = Tabel.getSelectedRow();
                model.removeRow(baris);}
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Tologn pilih data yang ingin dihapus");
        }

    }//GEN-LAST:event_btnHapusActionPerformed

