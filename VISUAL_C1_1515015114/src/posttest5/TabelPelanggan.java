    private void TabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TabelMouseClicked
        // TODO add your handling code here:
       int baris = Tabel.getSelectedRow();
        String Nim = Tabel.getValueAt(baris,0).toString();
        String Telpon = Tabel.getValueAt(baris,1).toString();
        String Kota = Tabel.getValueAt(baris,2).toString();
        txt_Nama.setText(Nim);
        txt_Tlp.setText(Telpon);
        cbKota.setSelectedItem(Kota);
         btnUpdate.setEnabled(true);
        btnHapus.setEnabled(true);
    }//GEN-LAST:event_TabelMouseClicked

    private void txt_TlpCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txt_TlpCaretUpdate
        // TODO add your handling code here:
        if (txt_Tlp.getText().length()== 0){
            btnSimpan.setEnabled(false);
        }
        else if (txt_Nama.getText().length()!=0 && txt_Tlp.getText().length()!=0){
            btnSimpan.setEnabled(true);
        }
    }//GEN-LAST:event_txt_TlpCaretUpdate

    private void txt_TlpKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_TlpKeyPressed
        // TODO add your handling code here:
                       txt_Tlp.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e){
            char k = e.getKeyChar();
            if(!((k >= '0') && (k <= '9')) || (k == '\b')){
                e.consume();
            }
            }
            
});
    }//GEN-LAST:event_txt_TlpKeyPressed

